package conditionalstatementsdemo.com;

public class SwitchDemo2 {

	public static void main(String[] args) {
		
		char c='R';
		
		
		switch(c)
		{
		case 'A':
			System.out.println("is a vowel");
			break;
		
		case 'E':
			System.out.println("is a vowel");
			break;
		
		case 'I':
			System.out.println("is a vowel");
			break;
		
		
		case 'O':
			System.out.println("is a vowel");
			break;
		
		case 'U':
			System.out.println("is a vowel");
			break;
		
	
			default:
				System.out.println("its consonant");
				break;
		
		}
		
		

	}

}
