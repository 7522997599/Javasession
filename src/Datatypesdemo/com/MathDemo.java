package Datatypesdemo.com;

public class MathDemo {

	public static void main(String[] args) {
		
		int a=2;
		int b=9;
		
		System.out.println(a+b);
		System.out.println(a*b);
		System.out.println(a/b);
		System.out.println(a%b);
		System.out.println(a-b);
		System.out.println(6/2);
		System.out.println(6.0/2);
		System.out.println(6/2.0);
		System.out.println(6.0/2.0);
		
		System.out.println((float)6/2);
		
		System.out.println(0/2);
		
		System.out.println(6%2);
		
		System.out.println(10%2);
		
		System.out.println(0%2);
		
		//System.out.println(10/0);//Arithmetic Exception /by zero
		//System.out.println(0/0);//Arithmetic Exception /by zero
		
		System.out.println(6.0/0);//Infinity
		System.out.println(0/0.0);//NaN
		System.out.println(0.0/0);//NaN
		System.out.println(0.0/0.0);//NaN
		System.out.println(6.0/0.0);//Infinity
		System.out.println(0.0/6.0);//
		System.out.println(6/0.0);//Infinity
		
		
		
		
		
	}

}
